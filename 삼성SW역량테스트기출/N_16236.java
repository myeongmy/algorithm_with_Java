/*문제
N×N 크기의 공간에 물고기 M마리와 아기 상어 1마리가 있다. 공간은 1×1 크기의 정사각형 칸으로 나누어져 있다. 한 칸에는 물고기가 최대 1마리 존재한다.

아기 상어와 물고기는 모두 크기를 가지고 있고, 이 크기는 자연수이다. 가장 처음에 아기 상어의 크기는 2이고, 아기 상어는 1초에 상하좌우로 인접한 한 칸씩 이동한다.

아기 상어는 자신의 크기보다 큰 물고기가 있는 칸은 지나갈 수 없고, 나머지 칸은 모두 지나갈 수 있다. 아기 상어는 자신의 크기보다 작은 물고기만 먹을 수 있다. 따라서, 크기가 같은 물고기는 먹을 수 없지만, 그 물고기가 있는 칸은 지나갈 수 있다.

아기 상어가 어디로 이동할지 결정하는 방법은 아래와 같다.

더 이상 먹을 수 있는 물고기가 공간에 없다면 아기 상어는 엄마 상어에게 도움을 요청한다.
먹을 수 있는 물고기가 1마리라면, 그 물고기를 먹으러 간다.
먹을 수 있는 물고기가 1마리보다 많다면, 거리가 가장 가까운 물고기를 먹으러 간다.
거리는 아기 상어가 있는 칸에서 물고기가 있는 칸으로 이동할 때, 지나야하는 칸의 개수의 최솟값이다.
거리가 가까운 물고기가 많다면, 가장 위에 있는 물고기, 그러한 물고기가 여러마리라면, 가장 왼쪽에 있는 물고기를 먹는다.
아기 상어의 이동은 1초 걸리고, 물고기를 먹는데 걸리는 시간은 없다고 가정한다. 즉, 아기 상어가 먹을 수 있는 물고기가 있는 칸으로 이동했다면, 이동과 동시에 물고기를 먹는다. 물고기를 먹으면, 그 칸은 빈 칸이 된다.

아기 상어는 자신의 크기와 같은 수의 물고기를 먹을 때 마다 크기가 1 증가한다. 예를 들어, 크기가 2인 아기 상어는 물고기를 2마리 먹으면 크기가 3이 된다.

공간의 상태가 주어졌을 때, 아기 상어가 몇 초 동안 엄마 상어에게 도움을 요청하지 않고 물고기를 잡아먹을 수 있는지 구하는 프로그램을 작성하시오.

입력
첫째 줄에 공간의 크기 N(2 ≤ N ≤ 20)이 주어진다.

둘째 줄부터 N개의 줄에 공간의 상태가 주어진다. 공간의 상태는 0, 1, 2, 3, 4, 5, 6, 9로 이루어져 있고, 아래와 같은 의미를 가진다.

0: 빈 칸
1, 2, 3, 4, 5, 6: 칸에 있는 물고기의 크기
9: 아기 상어의 위치
아기 상어는 공간에 한 마리 있다.

출력
첫째 줄에 아기 상어가 엄마 상어에게 도움을 요청하지 않고 물고기를 잡아먹을 수 있는 시간을 출력한다.*/

package 삼성SW역량테스트기출;

import java.util.*;

class Shark {
	int i; // 아기 상어의 현재 위치
	int j;
	int size; // 크기
	int ate; // 먹은 물고기 수

	Shark(int i, int j, int size, int ate) {
		this.i = i;
		this.j = j;
		this.size = size;
		this.ate = ate;
	}

}

class Point3 implements Comparable<Point3> {
	int i;
	int j;
	int cnt;

	Point3(int i, int j, int cnt) {
		this.i = i;
		this.j = j;
		this.cnt = cnt;
	}

	public int compareTo(Point3 p) {
		if (this.cnt < p.cnt) {
			return -1;
		} else if (this.cnt > p.cnt) {
			return 1;
		} else {
			if (this.i < p.i)
				return -1;
			else if (this.i == p.i) {
				if (this.j < p.j)
					return -1;
				else
					return 1;

			} else
				return 1;
		}
	}
}

public class N_16236 {
	static int[] dx = { -1, 0, 0, 1 };
	static int[] dy = { 0, -1, 1, 0 };

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		Scanner s = new Scanner(System.in);
		int N = s.nextInt();
		int[][] A = new int[N][N];
		Shark sh = new Shark(0, 0, 2, 0);
		for (int i = 0; i < N; i++) {
			for (int j = 0; j < N; j++) {
				A[i][j] = s.nextInt();
				if (A[i][j] == 9) {
					sh.i = i;
					sh.j = j;
				}

			}
		}

		int time = 0;
		while (true) {

			int[][] c = new int[N][N];
			PriorityQueue<Point3> q = new PriorityQueue<Point3>();
			int flag = 0;

			c[sh.i][sh.j] = 1;
			q.offer(new Point3(sh.i, sh.j, 0));

			while (!q.isEmpty()) {
				Point3 p = q.poll();
				if (A[p.i][p.j] != 0 && A[p.i][p.j] < sh.size) {
					A[sh.i][sh.j] = 0;
					sh.i = p.i;
					sh.j = p.j;
					sh.ate++;
					time += p.cnt;
					flag = 1;
					A[p.i][p.j] = 0;
					break;
				}
				for (int i = 0; i < dx.length; i++) {
					if (p.i + dx[i] >= 0 && p.i + dx[i] < N && p.j + dy[i] >= 0 && p.j + dy[i] < N) {
						if (A[p.i + dx[i]][p.j + dy[i]] <= sh.size && c[p.i + dx[i]][p.j + dy[i]] == 0) {
							c[p.i + dx[i]][p.j + dy[i]] = 1;
							q.offer(new Point3(p.i + dx[i], p.j + dy[i], p.cnt + 1));

						}
					}
				}
			}
			if (sh.ate == sh.size) {
				sh.size++;
				sh.ate = 0;
			}
			if (flag == 0)
				break;
		}
		System.out.println(time);
	}

}
