// BFS 문제(최단 거리 구하기)
/*문제
사악한 암흑의 군주 이민혁은 드디어 마법 구슬을 손에 넣었고, 그 능력을 실험해보기 위해 근처의 티떱숲에 홍수를 일으키려고 한다. 이 숲에는 고슴도치가 한 마리 살고 있다. 고슴도치는 제일 친한 친구인 비버의 굴로 가능한 빨리 도망가 홍수를 피하려고 한다.

티떱숲의 지도는 R행 C열로 이루어져 있다. 비어있는 곳은 '.'로 표시되어 있고, 물이 차있는 지역은 '*', 돌은 'X'로 표시되어 있다. 비버의 굴은 'D'로, 고슴도치의 위치는 'S'로 나타내어져 있다.

매 분마다 고슴도치는 현재 있는 칸과 인접한 네 칸 중 하나로 이동할 수 있다. (위, 아래, 오른쪽, 왼쪽) 물도 매 분마다 비어있는 칸으로 확장한다. 물이 있는 칸과 인접해있는 비어있는 칸(적어도 한 변을 공유)은 물이 차게 된다. 물과 고슴도치는 돌을 통과할 수 없다. 또, 고슴도치는 물로 차있는 구역으로 이동할 수 없고, 물도 비버의 소굴로 이동할 수 없다.

티떱숲의 지도가 주어졌을 때, 고슴도치가 안전하게 비버의 굴로 이동하기 위해 필요한 최소 시간을 구하는 프로그램을 작성하시오.

고슴도치는 물이 찰 예정인 칸으로 이동할 수 없다. 즉, 다음 시간에 물이 찰 예정인 칸으로 고슴도치는 이동할 수 없다. 이동할 수 있으면 고슴도치가 물에 빠지기 때문이다. 

입력
첫째 줄에 50보다 작거나 같은 자연수 R과 C가 주어진다.

다음 R개 줄에는 티떱숲의 지도가 주어지며, 문제에서 설명한 문자만 주어진다. 'D'와 'S'는 하나씩만 주어진다.

출력
첫째 줄에 고슴도치가 비버의 굴로 이동할 수 있는 가장 빠른 시간을 출력한다. 만약, 안전하게 비버의 굴로 이동할 수 없다면, "KAKTUS"를 출력한다.*/

package 백준;

import java.util.*;

class Point5 {
	int i;
	int j;
	int type; // 0이면 고슴도치, 1이면 물웅덩이

	Point5(int i, int j, int type) {
		this.i = i;
		this.j = j;
		this.type = type;
	}
}

public class N_3055 {
	static int[] dx = { 0, 0, -1, 1 };
	static int[] dy = { -1, 1, 0, 0 };

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		Scanner s = new Scanner(System.in);
		int R = s.nextInt();
		int C = s.nextInt();
		int[][] A = new int[R][C];
		Queue<Point5> q = new LinkedList<Point5>();
		int be_x = 0;
		int be_y = 0;
		for (int i = 0; i < R; i++) {
			String str = s.next();
			for (int j = 0; j < C; j++) {
				A[i][j] = str.charAt(j);
				if (A[i][j] == 'D') {
					be_x = i;
					be_y = j;
				}
			}
		}
		// 물웅덩이 큐에 넣기 -> 고슴도치가 다음 물이 찰 예정인 칸으로 이동 못하므로 큐에 물웅덩이부터 담는 것이 핵심이다.
		for (int i = 0; i < R; i++) {
			for (int j = 0; j < C; j++) {
				if (A[i][j] == '*')
					q.offer(new Point5(i, j, 1));
			}
		}
		int[][] c = new int[R][C];
		// 고슴도치 큐에 넣기
		for (int i = 0; i < R; i++) {
			for (int j = 0; j < C; j++) {
				if (A[i][j] == 'S') {
					q.offer(new Point5(i, j, 0));
					c[i][j] = 1;
				}
			}
		}

		while (!q.isEmpty()) {
			Point5 p = q.poll();
			if (p.i == be_x && p.j == be_y)
				break;
			if (p.type == 1) {
				for (int i = 0; i < dx.length; i++) {
					if (p.i + dx[i] >= 0 && p.i + dx[i] < A.length && p.j + dy[i] >= 0 && p.j + dy[i] < A[0].length) {
						if (A[p.i + dx[i]][p.j + dy[i]] == '.') {
							q.offer(new Point5(p.i + dx[i], p.j + dy[i], 1));
							A[p.i + dx[i]][p.j + dy[i]] = '*';
						}
					}
				}
			} else if (p.type == 0) {
				for (int i = 0; i < dx.length; i++) {
					if (p.i + dx[i] >= 0 && p.i + dx[i] < A.length && p.j + dy[i] >= 0 && p.j + dy[i] < A[0].length) {
						if ((A[p.i + dx[i]][p.j + dy[i]] == '.' || A[p.i + dx[i]][p.j + dy[i]] == 'D')
								&& c[p.i + dx[i]][p.j + dy[i]] == 0) {
							q.offer(new Point5(p.i + dx[i], p.j + dy[i], 0));
							c[p.i + dx[i]][p.j + dy[i]] = c[p.i][p.j] + 1;
						}
					}
				}
			}
		}
		if (c[be_x][be_y] == 0) {
			System.out.println("KAKTUS");
			System.exit(0);
		}
		System.out.println(c[be_x][be_y] - 1);

	}

}
